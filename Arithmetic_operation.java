public class Arithmetic_operation {
    public static void main(String[] args) {
        double x = 45;
        float y = 6f;
        int z = 2;
        System.out.println(x+y+z);
        System.out.println(x-y+z);
        System.out.println(x/y);
        System.out.println(x*y*z);
    }
}